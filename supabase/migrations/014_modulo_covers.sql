CREATE OR REPLACE FUNCTION public.crear_compra_cover_desde_pedido(
  p_cliente_id BIGINT,
  p_lugar_id BIGINT,
  p_items JSONB,
  p_subtotal NUMERIC,
  p_total NUMERIC,
  p_descuento_total NUMERIC DEFAULT 0,
  p_porcentaje_servicio NUMERIC DEFAULT 0,
  p_valor_servicio NUMERIC DEFAULT 0,
  p_cupon_id BIGINT DEFAULT NULL,
  p_wompi_cuenta_id BIGINT DEFAULT NULL,
  p_transaccion_checkout_id BIGINT DEFAULT NULL,
  p_confirmada BOOLEAN DEFAULT false
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_compra_id BIGINT;
  v_numero TEXT;
  v_item JSONB;
  v_sesion public.sesiones_cover%ROWTYPE;
  v_tipo_cover_id BIGINT;
  v_sesion_id BIGINT;
  v_cantidad INT;
  v_precio NUMERIC;
  v_i INT;
  v_bc public.boletas_cover%ROWTYPE;
  v_boletas JSONB := '[]'::jsonb;
  v_estado_pago TEXT;
  v_estado_compra TEXT;
  v_estado_boleta TEXT;
  v_now TIMESTAMPTZ := now();
BEGIN
  IF p_cliente_id IS NULL OR p_lugar_id IS NULL THEN
    RAISE EXCEPTION 'cliente_id y lugar_id son requeridos';
  END IF;

  IF p_items IS NULL OR jsonb_typeof(p_items) <> 'array' OR jsonb_array_length(p_items) = 0 THEN
    RAISE EXCEPTION 'items vacío';
  END IF;

  v_estado_pago := CASE WHEN p_confirmada THEN 'completado' ELSE 'pendiente' END;
  v_estado_compra := CASE WHEN p_confirmada THEN 'confirmada' ELSE 'pendiente' END;
  v_estado_boleta := CASE WHEN p_confirmada THEN 'activa' ELSE 'pendiente' END;

  v_numero := 'CCOV-' || to_char(v_now, 'YYYYMMDDHH24MISS') || '-' || floor(random() * 10000)::text;

  INSERT INTO public.compras_cover (
    cliente_id, lugar_id, numero_transaccion,
    subtotal, descuento_total, porcentaje_servicio, valor_servicio, total,
    cupon_id, estado_pago, estado_compra, wompi_cuenta_id,
    transaccion_checkout_id, fecha_confirmacion
  ) VALUES (
    p_cliente_id, p_lugar_id, v_numero,
    coalesce(p_subtotal, 0), coalesce(p_descuento_total, 0),
    coalesce(p_porcentaje_servicio, 0), coalesce(p_valor_servicio, 0), coalesce(p_total, 0),
    p_cupon_id, v_estado_pago, v_estado_compra, p_wompi_cuenta_id,
    p_transaccion_checkout_id,
    CASE WHEN p_confirmada THEN v_now ELSE NULL END
  )
  RETURNING id INTO v_compra_id;

  FOR v_item IN SELECT value FROM jsonb_array_elements(p_items)
  LOOP
    v_tipo_cover_id := (v_item->>'tipo_cover_id')::bigint;
    v_sesion_id := (v_item->>'sesion_cover_id')::bigint;
    v_cantidad := greatest(1, coalesce((v_item->>'cantidad')::int, 1));
    v_precio := coalesce((v_item->>'precio_unitario')::numeric, 0);

    SELECT * INTO v_sesion FROM public.sesiones_cover WHERE id = v_sesion_id FOR UPDATE;
    IF NOT FOUND THEN
      RAISE EXCEPTION 'Sesión cover % no encontrada', v_sesion_id;
    END IF;

    IF v_sesion.lugar_id IS DISTINCT FROM p_lugar_id THEN
      RAISE EXCEPTION 'Sesión % no pertenece al lugar %', v_sesion_id, p_lugar_id;
    END IF;

    IF v_sesion.tipo_cover_id IS DISTINCT FROM v_tipo_cover_id THEN
      RAISE EXCEPTION 'tipo_cover_id no coincide con la sesión %', v_sesion_id;
    END IF;

    IF v_sesion.estado NOT IN ('programada', 'abierta') THEN
      RAISE EXCEPTION 'Sesión % no disponible para venta', v_sesion_id;
    END IF;

    IF v_sesion.cantidad_maxima_venta IS NOT NULL
       AND v_sesion.cantidad_vendida + v_cantidad > v_sesion.cantidad_maxima_venta THEN
      RAISE EXCEPTION 'Cupo de venta agotado para sesión %', v_sesion_id;
    END IF;

    IF v_sesion.personas_dentro + v_cantidad > v_sesion.aforo_maximo THEN
      RAISE EXCEPTION 'Aforo insuficiente para sesión %', v_sesion_id;
    END IF;

    FOR v_i IN 1..v_cantidad LOOP
      INSERT INTO public.boletas_cover (
        compra_cover_id, sesion_cover_id, tipo_cover_id,
        codigo_qr, precio_unitario, estado, estado_acceso, titular_cliente_id
      ) VALUES (
        v_compra_id, v_sesion_id, v_tipo_cover_id,
        public.fn_generar_codigo_qr_cover(), v_precio, v_estado_boleta, 'pendiente', p_cliente_id
      )
      RETURNING * INTO v_bc;

      v_boletas := v_boletas || jsonb_build_array(jsonb_build_object(
        'id', v_bc.id,
        'codigo_qr', v_bc.codigo_qr,
        'sesion_cover_id', v_bc.sesion_cover_id
      ));
    END LOOP;

    UPDATE public.sesiones_cover
    SET cantidad_vendida = cantidad_vendida + v_cantidad, fecha_actualizacion = v_now
    WHERE id = v_sesion_id;
  END LOOP;

  RETURN jsonb_build_object(
    'compra_cover_id', v_compra_id,
    'numero_transaccion', v_numero,
    'boletas_cover', v_boletas
  );
END;
$$;

