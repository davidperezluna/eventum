CREATE OR REPLACE FUNCTION public.generar_sesiones_cover_desde_plantillas(
  p_hasta_fecha DATE DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_hasta DATE;
  v_creadas INT := 0;
  v_insertadas INT;
  rec RECORD;
  v_fecha DATE;
  v_aforo INT;
  v_apertura_ts TIMESTAMPTZ;
  v_cierre_ts TIMESTAMPTZ;
BEGIN
  IF NOT public.fn_usuario_es_admin()
     AND NOT EXISTS (
       SELECT 1 FROM public.usuarios u
       WHERE u.auth_user_id = auth.uid() AND u.tipo_usuario_id = 2 AND u.activo = true
     ) THEN
    RAISE EXCEPTION 'Sin permiso';
  END IF;

  v_hasta := coalesce(p_hasta_fecha, current_date + 21);

  FOR rec IN
    SELECT
      pl.*,
      tc.organizador_id,
      tc.precio_cop
    FROM public.plantillas_cover pl
    JOIN public.tipos_cover tc ON tc.id = pl.tipo_cover_id
    WHERE pl.activo = true
      AND tc.activo = true
  LOOP
    v_fecha := current_date;
    WHILE v_fecha <= v_hasta LOOP
      IF extract(isodow FROM v_fecha)::int = rec.dia_semana THEN
        v_aforo := coalesce(rec.aforo_maximo, public.fn_aforo_default_lugar(rec.lugar_id));

        SELECT h.hora_apertura_ts, h.hora_cierre_ts
        INTO v_apertura_ts, v_cierre_ts
        FROM public.fn_cover_horario_sesion(v_fecha, rec.hora_apertura, rec.hora_cierre) h;

        INSERT INTO public.sesiones_cover (
          plantilla_cover_id, tipo_cover_id, lugar_id, organizador_id,
          fecha, hora_apertura, hora_cierre, precio_cop, aforo_maximo,
          cantidad_maxima_venta, estado
        ) VALUES (
          rec.id,
          rec.tipo_cover_id,
          rec.lugar_id,
          rec.organizador_id,
          v_fecha,
          v_apertura_ts,
          v_cierre_ts,
          rec.precio_cop,
          v_aforo,
          rec.cantidad_maxima_venta,
          CASE WHEN v_fecha = current_date THEN 'abierta' ELSE 'programada' END
        )
        ON CONFLICT (tipo_cover_id, fecha) DO NOTHING;

        GET DIAGNOSTICS v_insertadas = ROW_COUNT;
        IF v_insertadas > 0 THEN
          v_creadas := v_creadas + 1;
        END IF;
      END IF;
      v_fecha := v_fecha + 1;
    END LOOP;
  END LOOP;

  RETURN jsonb_build_object('sesiones_creadas', v_creadas, 'hasta_fecha', v_hasta);
END;
$$;

CREATE OR REPLACE FUNCTION public.cambiar_estado_sesion_cover(
  p_sesion_id BIGINT,
  p_estado TEXT
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_sesion public.sesiones_cover%ROWTYPE;
BEGIN
  IF p_estado NOT IN ('programada', 'abierta', 'cerrada', 'cancelada') THEN
    RAISE EXCEPTION 'Estado inválido';
  END IF;

  SELECT * INTO v_sesion FROM public.sesiones_cover WHERE id = p_sesion_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Sesión no encontrada';
  END IF;

  IF NOT public.fn_usuario_puede_gestionar_cover(v_sesion.organizador_id) THEN
    RAISE EXCEPTION 'Sin permiso';
  END IF;

  UPDATE public.sesiones_cover
  SET estado = p_estado, fecha_actualizacion = now()
  WHERE id = p_sesion_id
  RETURNING * INTO v_sesion;

  IF p_estado = 'cerrada' THEN
    UPDATE public.boletas_cover bc
    SET
      estado_acceso = 'consumida',
      fecha_actualizacion = now()
    WHERE bc.sesion_cover_id = p_sesion_id
      AND bc.estado_acceso IN ('pendiente', 'fuera');
  END IF;

  RETURN row_to_json(v_sesion)::jsonb;
END;
$$;

