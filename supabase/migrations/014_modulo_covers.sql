CREATE TABLE IF NOT EXISTS public.sesiones_cover (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  plantilla_cover_id BIGINT REFERENCES public.plantillas_cover(id) ON DELETE SET NULL,
  tipo_cover_id BIGINT NOT NULL REFERENCES public.tipos_cover(id) ON DELETE RESTRICT,
  lugar_id BIGINT NOT NULL REFERENCES public.lugares(id) ON DELETE CASCADE,
  organizador_id BIGINT NOT NULL REFERENCES public.usuarios(id) ON DELETE RESTRICT,
  fecha DATE NOT NULL,
  hora_apertura TIMESTAMPTZ NOT NULL,
  hora_cierre TIMESTAMPTZ NOT NULL,
  precio_cop BIGINT NOT NULL CHECK (precio_cop >= 0),
  aforo_maximo INTEGER NOT NULL CHECK (aforo_maximo >= 1),
  personas_dentro INTEGER NOT NULL DEFAULT 0 CHECK (personas_dentro >= 0),
  cantidad_vendida INTEGER NOT NULL DEFAULT 0 CHECK (cantidad_vendida >= 0),
  cantidad_maxima_venta INTEGER CHECK (cantidad_maxima_venta IS NULL OR cantidad_maxima_venta >= 0),
  estado TEXT NOT NULL DEFAULT 'programada'
    CHECK (estado IN ('programada', 'abierta', 'cerrada', 'cancelada')),
  fecha_creacion TIMESTAMPTZ NOT NULL DEFAULT now(),
  fecha_actualizacion TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (tipo_cover_id, fecha),
  CHECK (hora_cierre > hora_apertura),
  CHECK (personas_dentro <= aforo_maximo)
);

