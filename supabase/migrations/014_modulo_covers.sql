CREATE TABLE IF NOT EXISTS public.tipos_cover (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  lugar_id BIGINT NOT NULL REFERENCES public.lugares(id) ON DELETE CASCADE,
  organizador_id BIGINT NOT NULL REFERENCES public.usuarios(id) ON DELETE RESTRICT,
  nombre TEXT NOT NULL CHECK (char_length(trim(nombre)) >= 2),
  descripcion TEXT,
  precio_cop BIGINT NOT NULL CHECK (precio_cop >= 0),
  permite_reingreso BOOLEAN NOT NULL DEFAULT true,
  limite_por_persona INTEGER CHECK (limite_por_persona IS NULL OR limite_por_persona >= 1),
  orden INTEGER NOT NULL DEFAULT 0,
  activo BOOLEAN NOT NULL DEFAULT true,
  wompi_cuenta_id BIGINT REFERENCES public.wompi_cuentas(id) ON DELETE SET NULL,
  fecha_creacion TIMESTAMPTZ NOT NULL DEFAULT now(),
  fecha_actualizacion TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_tipos_cover_lugar_activo
  ON public.tipos_cover(lugar_id, orden)
  WHERE activo = true;

CREATE INDEX IF NOT EXISTS idx_tipos_cover_organizador
  ON public.tipos_cover(organizador_id, fecha_creacion DESC);

CREATE TABLE IF NOT EXISTS public.plantillas_cover (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tipo_cover_id BIGINT NOT NULL REFERENCES public.tipos_cover(id) ON DELETE CASCADE,
  lugar_id BIGINT NOT NULL REFERENCES public.lugares(id) ON DELETE CASCADE,
  -- ISO 8601: 1 = lunes … 7 = domingo
  dia_semana SMALLINT NOT NULL CHECK (dia_semana BETWEEN 1 AND 7),
  hora_apertura TIME NOT NULL,
  hora_cierre TIME NOT NULL,
  aforo_maximo INTEGER CHECK (aforo_maximo IS NULL OR aforo_maximo >= 1),
  cantidad_maxima_venta INTEGER CHECK (cantidad_maxima_venta IS NULL OR cantidad_maxima_venta >= 0),
  -- Cuántos días hacia adelante generar sesiones automáticas.
  dias_anticipacion INTEGER NOT NULL DEFAULT 21 CHECK (dias_anticipacion BETWEEN 1 AND 90),
  activo BOOLEAN NOT NULL DEFAULT true,
  fecha_creacion TIMESTAMPTZ NOT NULL DEFAULT now(),
  fecha_actualizacion TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_plantillas_cover_tipo
  ON public.plantillas_cover(tipo_cover_id)
  WHERE activo = true;

CREATE INDEX IF NOT EXISTS idx_plantillas_cover_lugar_dia
  ON public.plantillas_cover(lugar_id, dia_semana)
  WHERE activo = true;

