      dias_anticipacion = coalesce(p_dias_anticipacion, dias_anticipacion),
      activo = coalesce(p_activo, activo),
      fecha_actualizacion = now()
    WHERE id = p_id
    RETURNING * INTO v_row;
  END IF;

  RETURN row_to_json(v_row)::jsonb;
END;
