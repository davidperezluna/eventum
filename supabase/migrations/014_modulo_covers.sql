      l.activo
    FROM public.lugares l
    WHERE l.id = p_lugar_id
  ) l;

  IF v_lugar IS NULL THEN
    RETURN NULL;
  END IF;

  SELECT coalesce(jsonb_agg(row_to_json(t)::jsonb ORDER BY t.orden, t.nombre), '[]'::jsonb)
  INTO v_tipos
