CREATE OR REPLACE FUNCTION public.actualizar_sesion_cover(
  p_sesion_id BIGINT,
  p_hora_apertura TIME DEFAULT NULL,
  p_hora_cierre TIME DEFAULT NULL,
  p_aforo_maximo INTEGER DEFAULT NULL,
  p_cantidad_maxima_venta INTEGER DEFAULT NULL,
  p_precio_cop BIGINT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_sesion public.sesiones_cover%ROWTYPE;
  v_apertura_time TIME;
  v_cierre_time TIME;
  v_apertura_ts TIMESTAMPTZ;
  v_cierre_ts TIMESTAMPTZ;
BEGIN
  SELECT * INTO v_sesion FROM public.sesiones_cover WHERE id = p_sesion_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Sesión no encontrada';
  END IF;

  IF NOT public.fn_usuario_puede_gestionar_cover(v_sesion.organizador_id) THEN
    RAISE EXCEPTION 'Sin permiso';
  END IF;

  IF v_sesion.estado NOT IN ('programada', 'abierta') THEN
    RAISE EXCEPTION 'Solo se editan sesiones programadas o abiertas';
  END IF;

  IF p_aforo_maximo IS NOT NULL AND p_aforo_maximo < v_sesion.personas_dentro THEN
    RAISE EXCEPTION 'El aforo no puede ser menor que personas dentro (%)', v_sesion.personas_dentro;
  END IF;

  v_apertura_time := coalesce(p_hora_apertura, v_sesion.hora_apertura::time);
  v_cierre_time := coalesce(p_hora_cierre, v_sesion.hora_cierre::time);

  IF p_hora_apertura IS NOT NULL OR p_hora_cierre IS NOT NULL THEN
    SELECT h.hora_apertura_ts, h.hora_cierre_ts
    INTO v_apertura_ts, v_cierre_ts
    FROM public.fn_cover_horario_sesion(v_sesion.fecha, v_apertura_time, v_cierre_time) h;
  END IF;

  UPDATE public.sesiones_cover
  SET
    hora_apertura = CASE
      WHEN p_hora_apertura IS NULL AND p_hora_cierre IS NULL THEN hora_apertura
      ELSE v_apertura_ts
    END,
    hora_cierre = CASE
      WHEN p_hora_apertura IS NULL AND p_hora_cierre IS NULL THEN hora_cierre
      ELSE v_cierre_ts
    END,
    aforo_maximo = coalesce(p_aforo_maximo, aforo_maximo),
    cantidad_maxima_venta = coalesce(p_cantidad_maxima_venta, cantidad_maxima_venta),
    precio_cop = coalesce(p_precio_cop, precio_cop),
    fecha_actualizacion = now()
  WHERE id = p_sesion_id
  RETURNING * INTO v_sesion;

  RETURN row_to_json(v_sesion)::jsonb;
END;
$$;

CREATE OR REPLACE FUNCTION public.inicializar_cover_lugar(
  p_lugar_id BIGINT,
  p_nombre_tipo TEXT,
  p_precio_cop BIGINT,
