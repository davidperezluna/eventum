-- Convierte fecha + horas TIME a TIMESTAMPTZ; si cierre < apertura, cierre es al día siguiente.
CREATE OR REPLACE FUNCTION public.fn_cover_horario_sesion(
  p_fecha DATE,
  p_hora_apertura TIME,
  p_hora_cierre TIME
)
RETURNS TABLE(hora_apertura_ts TIMESTAMPTZ, hora_cierre_ts TIMESTAMPTZ)
LANGUAGE plpgsql
IMMUTABLE
SET search_path = public
AS $$
BEGIN
  IF p_fecha IS NULL OR p_hora_apertura IS NULL OR p_hora_cierre IS NULL THEN
    RAISE EXCEPTION 'fecha, hora_apertura y hora_cierre son requeridos';
  END IF;

  IF p_hora_cierre = p_hora_apertura THEN
    RAISE EXCEPTION 'La hora de cierre debe ser distinta a la de apertura';
  END IF;

  hora_apertura_ts := (p_fecha + p_hora_apertura) AT TIME ZONE 'America/Bogota';

  IF p_hora_cierre > p_hora_apertura THEN
    hora_cierre_ts := (p_fecha + p_hora_cierre) AT TIME ZONE 'America/Bogota';
  ELSE
    hora_cierre_ts := ((p_fecha + 1) + p_hora_cierre) AT TIME ZONE 'America/Bogota';
  END IF;

  RETURN NEXT;
END;
$$;

