  p_hora_apertura TIME,
  p_hora_cierre TIME,
  p_covers_descripcion TEXT DEFAULT NULL,
  p_aforo_maximo INTEGER DEFAULT NULL,
  p_cantidad_maxima_venta INTEGER DEFAULT NULL,
  p_permite_reingreso BOOLEAN DEFAULT true,
