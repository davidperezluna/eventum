-- ─── 5. Asegurar columnas del checkout independiente (por si faltó 016) ─────

ALTER TABLE public.tipos_cover
  ADD COLUMN IF NOT EXISTS wompi_cuenta_id BIGINT
  REFERENCES public.wompi_cuentas(id) ON DELETE SET NULL;

ALTER TABLE public.transacciones_checkout
  ADD COLUMN IF NOT EXISTS lugar_id BIGINT
  REFERENCES public.lugares(id) ON DELETE SET NULL;

ALTER TABLE public.transacciones_checkout
