ALTER TABLE public.sesiones_cover
  DROP COLUMN IF EXISTS evento_id;

ALTER TABLE public.sesiones_cover
  DROP COLUMN IF EXISTS tipo_boleta_id;

-- ─── 4. Marcador en eventos ─────────────────────────────────────────────────

ALTER TABLE public.eventos
