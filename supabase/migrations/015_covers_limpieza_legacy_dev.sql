  BIGINT, BIGINT, TEXT, TEXT, BIGINT, BOOLEAN, INTEGER, INTEGER, BOOLEAN, BIGINT, BIGINT
);

DROP FUNCTION IF EXISTS public.upsert_tipo_cover(
  BIGINT, BIGINT, TEXT, TEXT, BIGINT, BOOLEAN, INTEGER, INTEGER, BOOLEAN, BIGINT, BIGINT, BIGINT
);

-- ─── 2. Puente tipos_boleta ↔ tipos_cover ────────────────────────────────────

DROP INDEX IF EXISTS public.idx_tipos_boleta_tipo_cover;

ALTER TABLE public.tipos_boleta
